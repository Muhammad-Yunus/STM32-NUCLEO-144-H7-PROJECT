/* ═══════════════════════════════════════════════════════════════════════════
 *  Core: single pixel draw
 * ═══════════════════════════════════════════════════════════════════════════ */

void GUI_DrawPixel(uint16_t x, uint16_t y, uint16_t color)
{
    if (x >= lcddev.width || y >= lcddev.height) return;
    LCD_DrawPoint(x, y);
}

/* ═══════════════════════════════════════════════════════════════════════════
 *  Fast lines (Adafruit GFX style)
 * ═══════════════════════════════════════════════════════════════════════════ */

void GUI_DrawFastVLine(uint16_t x, uint16_t y, uint16_t h, uint16_t color)
{
    if (h == 0) return;
    if (x >= lcddev.width || y >= lcddev.height) return;
    if (y + h > lcddev.height) h = lcddev.height - y;

    LCD_SetWindows(x, y, x, y + h - 1);
    for (uint32_t i = 0; i < h; i++)
        LCD->LCD_RAM = color;
}

void GUI_DrawFastHLine(uint16_t x, uint16_t y, uint16_t w, uint16_t color)
{
    if (w == 0) return;
    if (x >= lcddev.width || y >= lcddev.height) return;
    if (x + w > lcddev.width) w = lcddev.width - x;

    LCD_SetWindows(x, y, x + w - 1, y);
    for (uint32_t i = 0; i < w; i++)
        LCD->LCD_RAM = color;
}

